export const lessonEntities = mockScheduledLessons;

export type MockLesson = {
  id: number;
  title: string;
  proficiencyLevelId: ProficiencyLevelId;
  duration: number;
  xp: number;
  difficulty: 'easy' | 'medium' | 'hard';
  description: string;
  completed: boolean;
  locked: boolean;
  visibilityByRole: UserRole[];
};

export const mockLessons: MockLesson[] = mockScheduledLessons.map((lesson) => ({
  id: lesson.lessonId ?? lesson.id,
  title: lesson.title,
  proficiencyLevelId: PROFICIENCY_LEVEL.b2.id,
  duration: lesson.duration,
  xp: 30,
  difficulty: 'medium',
  description: lesson.description ?? lesson.notes ?? `${lesson.title} lesson`,
  completed: lesson.statusId === LESSON_STATUS.completed.id,
  locked: false,
  visibilityByRole: [
    USER_ROLE.student.id,
    USER_ROLE.teacher.id,
    USER_ROLE.admin.id,
    USER_ROLE.superAdmin.id,
  ],
}));

/** Single-tenant mock: all catalog lessons are visible. */
export const getLessonsForMock = (): MockLesson[] => mockLessons;

/** @deprecated Use getLessonsForMock — account scope removed. */
export const getLessonsForAccount = (_accountId?: string): MockLesson[] => mockLessons;

export function getStudentScheduledLessons(
  studentId: number,
): ScheduledLessonDto[] {
  return mockScheduledLessons.filter((lesson) => lesson.studentId === studentId);
}
